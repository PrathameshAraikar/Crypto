extension DetailView {
    
    private var navigationbarTrailingItems: some View {
        HStack {
            Text(vm.coin.symbol.uppercased())
                .font(.headline)
                .foregroundColor(Color.theme.secondaryText)
            
            CoinImageView(coin: vm.coin)
                .frame(width: 25, height: 25)
        }
    }
    
    private var overviewTitle: some View {
        Text("Overview")
            .font(.title)
            .bold()
            .foregroundColor(Color.theme.accent)
            .frame(maxWidth: .infinity, alignment: .leading)
    }
    
    private var additionalTitle: some View {
        Text("Additional Details")
            .font(.title)
            .bold()
            .foregroundColor(Color.theme.accent)
            .frame(maxWidth: .infinity, alignment: .leading)
    }
    
    private var descriptionSection: some View {
        VStack {
            if let description = vm.coinDescription, !description.isEmpty {
                VStack(alignment: .leading) {
                    Text(description)
                        .lineLimit(showFullDescription ? nil : 3)
                        .font(.callout)
                        .foregroundColor(Color.theme.secondaryText)
                    
                    Button {
                        withAnimation(.easeInOut) {
                            showFullDescription.toggle()
                        }
                    } label: {
                        Text(showFullDescription ? "Show Less" : "Read More")
                            .font(.caption)
                            .fontWeight(.bold)
                            .padding(.vertical, 4)
                            .foregroundColor(Color.blue)
                    }

                }
                .frame(maxWidth: .infinity, alignment: .leading)
            }
        }
    }
    
    private var overviewGrid: some View {
        LazyVGrid(
            columns: columns,
            alignment: .leading,
            spacing: spacing,
            pinnedViews: []) {
                ForEach(vm.overviewStatistics) { stat in
                    StatisticView(stat: stat)
                }
            }
    }
    
    private var additionalGrid: some View {
        LazyVGrid(
            columns: columns,
            alignment: .leading,
            spacing: spacing,
            pinnedViews: []) {
                ForEach(vm.additionalStatistics) { stat in
                    StatisticView(stat: stat)
                }
            }
    }
    
    private var linksSection: some View {
        VStack(alignment: .leading, spacing: 20.0) {
            if let websiteString = vm.websiteURL,
               !websiteString.isEmpty,
               let websiteURL = URL(string: websiteString) {
                Link("Website", destination: websiteURL)
            }
            
            if let redditString = vm.redditURL,
               !redditString.isEmpty,
               let redditURL = URL(string: redditString) {
                Link("Reddit", destination: redditURL)
            }
        }
        .foregroundColor(Color.blue)
        .font(.headline)
        .frame(maxWidth: .infinity, alignment: .leading)
    }
}
